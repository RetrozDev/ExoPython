#Exercice 1 : Afficher un message
#Écrire un programme qui affiche "Bonjour, monde !".



#Exercice 2 : Addition de deux nombres
#Écrire un programme qui demande à l'utilisateur d'entrer deux nombres et qui affiche leur somme.



#Exercice 3 : Calculer la moyenne
#Écrire un programme qui demande à l'utilisateur d'entrer trois nombres et qui affiche leur moyenne.



#Exercice 4 : Vérifier un nombre pair ou impair
#Écrire un programme qui demande à l'utilisateur d'entrer un nombre et qui affiche si ce nombre est pair ou impair.



#Exercice 5 : Table de multiplication
#Écrire un programme qui demande à l'utilisateur d'entrer un nombre et qui affiche la table de multiplication de ce nombre jusqu'à 10.



#Exercice 6 : Compter les voyelles
#Écrire un programme qui demande à l'utilisateur d'entrer une phrase et qui compte le nombre de voyelles dans cette phrase.



#Exercice 7 : Inverser une chaîne
#Écrire un programme qui demande à l'utilisateur d'entrer une chaîne de caractères et qui affiche cette chaîne inversée.



#Exercice 8 : Trouver le plus grand nombre
#Écrire un programme qui demande à l'utilisateur d'entrer trois nombres et qui affiche le plus grand de ces trois nombres.



# Exercice 9 : Calculer la factorielle
# Écrire un programme qui demande à l'utilisateur d'entrer un nombre et qui calcule et affiche la factorielle de ce nombre.



#Exercice 10 : Deviner le nombre
#Écrire un programme qui génère un nombre aléatoire entre 1 et 100 et qui demande à l'utilisateur de deviner ce nombre. Le programme doit indiquer si la devinette est trop basse, trop haute ou correcte.